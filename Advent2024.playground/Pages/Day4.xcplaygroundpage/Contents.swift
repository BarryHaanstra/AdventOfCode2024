func countOccurrencesOfXMAS(in grid: [[String]]) -> Int {
    var total = 0

    for (y, line) in grid.enumerated() {
        for (x, letter) in line.enumerated() {
            if letter == "A" {
                if checkPatternAround(x: x, y: y, on: grid) {
                    total += 1
                }
            }
        }
    }

    return total
}

func checkPatternAround(x: Int, y: Int, on grid: [[String]]) -> Bool {
    if let letter1 = letterFrom(x: x - 1, y: y - 1, on: grid),
       let letter2 = letterFrom(x: x + 1, y: y - 1, on: grid),
       let letter3 = letterFrom(x: x - 1, y: y + 1, on: grid),
       let letter4 = letterFrom(x: x + 1, y: y + 1, on: grid)
    {
        if (letter1 == "M" && letter4 == "S" && letter3 == "M" && letter2 == "S") ||  // MAS, MAS
            (letter1 == "M" && letter4 == "S" && letter3 == "S" && letter2 == "M") || // MAS, SAM
            (letter1 == "S" && letter4 == "M" && letter3 == "M" && letter2 == "S") || // SAM, MAS
            (letter1 == "S" && letter4 == "M" && letter3 == "S" && letter2 == "M")    // SAM, SAM
        {
            return true
        }
    }

    return false
}

func letterFrom(x: Int, y: Int, on grid: [[String]]) -> String? {
    if validCoordinate(x: x, y: y, on: grid) {
        return grid[y][x]
    }
    return nil
}


// MARK: - Helpers

struct StartPoint {
    let startX: Int
    let startY: Int
    let dirX: Int
    let dirY: Int
}

func validCoordinate(x: Int, y: Int, on grid: [[String]]) -> Bool {
    return x >= 0 && x < grid[0].count && y >= 0 && y < grid.count
}
