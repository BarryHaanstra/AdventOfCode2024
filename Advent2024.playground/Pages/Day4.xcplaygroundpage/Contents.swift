func countOccurrencesOf(word: String, in grid: [[String]]) -> Int {
    var total = 0
    var startPoints = [StartPoint]()

    // Horizontal
    for y in 0 ..< grid.count {
        startPoints.append(StartPoint(startX: 0, startY: y, dirX: 1, dirY: 0))
    }

    // Vertical
    for x in 0 ..< grid[0].count {
        startPoints.append(StartPoint(startX: x, startY: 0, dirX: 0, dirY: 1))
    }

    // Diagonal down
    for x in 0 ..< grid[0].count {
        startPoints.append(StartPoint(startX: x, startY: 0, dirX: 1, dirY: 1))
    }
    for y in 1 ..< grid.count { // Don't start from 0, that line is already done
        startPoints.append(StartPoint(startX: 0, startY: y, dirX: 1, dirY: 1))
    }

    // Diagonal up
    for x in 0 ..< grid[0].count {
        startPoints.append(StartPoint(startX: x, startY: grid.count - 1, dirX: 1, dirY: -1))
    }
    for y in 0 ..< (grid.count - 1) { // Don't go down to "grid.count", that line is already done
        startPoints.append(StartPoint(startX: 0, startY: y, dirX: 1, dirY: -1))
    }

    // Count
    for startPoint in startPoints {
        let line = makeString(of: grid, startX: startPoint.startX, startY: startPoint.startY, dirX: startPoint.dirX, dirY: startPoint.dirY)
        total += countOccurrencesOf(word: word, in: line)
    }

    return total
}

func makeString(of grid: [[String]], startX: Int, startY: Int, dirX: Int, dirY: Int) -> String {
    var currentX = startX
    var currentY = startY
    var line = ""

    while validCoordinate(x: currentX, y: currentY, on: grid) {
        line.append(grid[currentY][currentX])
        currentX += dirX
        currentY += dirY
    }

    return line
}

func countOccurrencesOf(word: String, in line: String) -> Int {
    var count = line.components(separatedBy: word).count - 1

    // Count BOTH directions!
    count += String(line.reversed()).components(separatedBy: word).count - 1

    return count
}


